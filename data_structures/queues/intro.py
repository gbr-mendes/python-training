# O conceito é o de uma fila comum
# First in first out (FIFO) ou last in last out (LILO)
# Seus métodos são enqueue(entrar na fila) e dequeue(sair/remover da fila)
# Para que tenhamos uma fila, precisamos adicionar e remover elementos em pontas diferentes
# Portando, usando os conceitos de linked list, o big0 de filas pode chegar a 0(1) nos dois métodos